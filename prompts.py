# ============================================================
# АНАЛИЗ ПОСТА — основной разбор (4 варианта по типу контента)
# ============================================================

_ANALYSIS_ROLE = "Ты — опытный контент-аналитик соцсетей. "
_NO_HALLUCINATION = (
    "Опирайся только на предоставленные данные. Если данных не хватает — "
    "пиши «недостаточно данных», не выдумывай факты.\n\n"
)


def build_analysis_prompt(caption: str, *, has_speech: bool, has_visual: bool,
                          is_carousel: bool) -> str:
    """Собрать промпт разбора поста под конкретный тип контента.

    caption уже размечен тегами [Что видно в кадре] / [Что говорит человек]
    для видео, либо содержит исходный текст поста / разбор слайдов карусели.
    """
    if is_carousel:
        return (
            _ANALYSIS_ROLE +
            "Это пост Instagram с каруселью (несколько кадров). В тексте ниже — "
            "подпись автора и разбор слайдов: OCR с картинок и транскрипты речи "
            "из видео-слайдов.\n" + _NO_HALLUCINATION +
            "Свяжи слайды в единую мысль и посыл. Ответь на русском языке:\n\n"
            "СУТЬ ПУБЛИКАЦИИ:\n[2–4 предложения]\n\n"
            "ЛОГИКА ПО СЛАЙДАМ:\n[кратко: что на каждом этапе и зачем]\n\n"
            "КЛЮЧЕВЫЕ ИДЕИ:\n[3–5 тезисов через •]\n\n"
            "ИДЕИ ДЛЯ СВОЕГО КОНТЕНТА:\n[2–3 идеи]\n\n"
            f"---\n{caption[:12000]}"
        )

    if has_speech and has_visual:
        return (
            _ANALYSIS_ROLE +
            "Ты анализируешь видео-пост из Instagram. У тебя два источника:\n"
            "• [Что видно в кадре] — ВИЗУАЛЬНЫЙ РЯД (обстановка, антураж, подача)\n"
            "• [Что говорит человек] — РЕЧЬ АВТОРА (ГЛАВНЫЙ источник смысла)\n\n"
            "Правило: если визуал и речь о разном — суть берём из РЕЧИ. "
            "Визуал упоминаем только как формат/стиль подачи.\n" + _NO_HALLUCINATION +
            "Ответь строго в этом формате на русском языке:\n\n"
            "ФОРМАТ И СТИЛЬ ПОДАЧИ:\n"
            "[1 строка: как снято, кто, где, какой тип контента]\n\n"
            "СУТЬ (из речи):\n"
            "[2–3 предложения — главная мысль и посыл автора]\n\n"
            "КЛЮЧЕВЫЕ ТЕЗИСЫ:\n"
            "[3–5 конкретных тезисов из речи автора, через •]\n\n"
            "ИДЕИ ДЛЯ СВОЕГО КОНТЕНТА:\n"
            "[2–3 конкретные идеи как адаптировать эту тему или формат под себя]\n\n"
            f"---\n{caption[:3500]}"
        )

    if has_visual:
        return (
            _ANALYSIS_ROLE +
            "Это превью видео-поста из Instagram без распознанной речи.\n" +
            _NO_HALLUCINATION +
            "Чётко отделяй наблюдаемое от догадок: вывод по визуалу помечай как "
            "вероятный.\nОтветь на русском языке:\n\n"
            "ФОРМАТ И СТИЛЬ:\n[тип контента, подача]\n\n"
            "О ЧЁМ ВЕРОЯТНО:\n[гипотеза по визуалу]\n\n"
            "ИДЕИ ДЛЯ СВОЕГО КОНТЕНТА:\n[2–3 идеи]\n\n"
            f"---\n{caption[:2500]}"
        )

    # Обычный текстовый пост
    return (
        _ANALYSIS_ROLE +
        "Это пост из Instagram.\n" + _NO_HALLUCINATION +
        "Ответь на русском языке:\n\n"
        "СУТЬ:\n[2–3 предложения — главная мысль]\n\n"
        "КЛЮЧЕВЫЕ ИДЕИ:\n[3–5 тезисов через •]\n\n"
        "ИДЕИ ДЛЯ СВОЕГО КОНТЕНТА:\n[2–3 конкретные идеи]\n\n"
        f"---\n{caption[:2500]}"
    )


# ============================================================
# КНОПКИ быстрого анализа (Резюме / Идеи / Перевести)
# ============================================================

def build_quick_action_prompt(action: str, caption: str) -> str:
    """Промпты для кнопок Резюме / Идеи / Перевести.

    caption уже обогащён описанием кадра и транскриптом для видео-постов.
    """
    prompts = {
        "analyze_summary": (
            "Ты — контент-аналитик. Сделай краткое резюме этого поста в 2–3 "
            "предложениях на русском языке. Только суть, без вводных слов типа "
            "«В этом посте…». Не добавляй фактов, которых нет в тексте:\n\n" + caption
        ),
        "analyze_ideas": (
            "Ты — контент-стратег. На основе этого поста предложи 3–5 конкретных "
            "идей для своего контента на похожую тему.\n\n"
            "Пример хорошего ответа (формат, а не тема):\n"
            "1. Reel «3 ошибки новичков в X» — короткие подписи на экране, динамичный монтаж.\n"
            "2. Карусель «Чек-лист из 7 пунктов по Y» — один пункт на слайд.\n\n"
            "Теперь дай идеи под этот пост. Каждую с новой строки, конкретно, "
            "по-русски:\n\n" + caption
        ),
        "analyze_adapt": (
            "Ты — редактор. Переведи этот текст на русский язык (если он не на "
            "русском) и адаптируй для русскоязычной аудитории. Сохрани смысл, но "
            "сделай живо и естественно. Ничего не добавляй от себя:\n\n" + caption
        ),
    }
    return prompts.get(action, prompts["analyze_summary"])


