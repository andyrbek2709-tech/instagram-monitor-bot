REVERSE_SYSTEM_PROMPT = (
    "Ты — AI Reverse Engineering Analyst. Анализируешь контент как reverse engineer: "
    "восстанавливаешь скрытые технологии, workflow, архитектуру и AI-механику. "
    "НЕ пересказывай контент. НЕ фокусируйся на маркетинге, курсах, CTA, эмоциях. "
    "Ищи: реальный стек, автоматизацию, AI-агенты, orchestration, hidden infrastructure. "
    "Каждое утверждение помечай: [факт] — если прямо следует из данных, "
    "[гипотеза] — если это достройка по косвенным признакам. Не выдавай гипотезы за факты."
)


def build_reverse_prompt(*, account: str, likes, comments, caption: str) -> str:
    """Собрать user-промпт для технического реверс-инжиниринга.

    caption обогащён описанием кадра и транскриптом речи (теги в тексте),
    поэтому реверс работает и по видео без подписи.
    """
    body = caption[:4000] if caption else "(текст отсутствует — только медиа)"
    return (
        "Проведи технический реверс-инжиниринг. "
        "Ответь строго в формате (каждая секция — 2–5 строк, без воды, "
        "максимум конкретики):\n\n"
        "1. CORE SYSTEM IDEA\n"
        "Что на самом деле демонстрирует автор — не тема поста, а суть системы.\n\n"
        "2. HIDDEN MECHANISM\n"
        "Какая реальная механика скрыта за контентом.\n\n"
        "3. IMPLIED ARCHITECTURE\n"
        "Как вероятно устроена система технически (компоненты, связи, паттерны).\n\n"
        "4. AUTOMATION & AGENT PATTERNS\n"
        "Какие automation/AI-agent подходы используются или подразумеваются.\n\n"
        "5. TOOLS / STACK\n"
        "Конкретные технологии, API, библиотеки, AI-модели, сервисы.\n\n"
        "6. WORKFLOW RECONSTRUCTION\n"
        "Полный workflow от начала до конца — пошагово.\n\n"
        "7. WHAT IS NOT SHOWN\n"
        "Что автор скрывает, но что почти наверняка существует в системе.\n\n"
        "8. HOW TO REBUILD IT\n"
        "Как реализовать подобную систему — конкретный план без воды.\n\n"
        "--- КОНТЕНТ ---\n"
        f"Аккаунт: {account} | Лайки: {likes} | Комментарии: {comments}\n\n"
        f"{body}"
    )


# ============================================================
# СОЗДАТЬ ПРОМПТ — готовый промпт для AI-агента
# ============================================================

def build_create_prompt(*, account: str, likes, comments, caption: str,
                        analysis: str) -> str:
    """Собрать мета-промпт, который генерирует готовый промпт для AI-агента."""
    return (
        "На основе анализа Instagram поста создай промпт для AI-агента.\n\n"
        "ГЛАВНОЕ ПРАВИЛО: Вытащи ВСЮ конкретику из поста — названия инструментов, "
        "фичи, бизнес-модель, целевую аудиторию, цифры. Не обобщай и не выдумывай "
        "того, чего нет в данных!\n\n"
        "СТРУКТУРА ПРОМПТА:\n\n"
        "Ты [роль на основе поста: разработчик/дизайнер/маркетолог/предприниматель и т.д.].\n\n"
        "Я увидел пост про [конкретная тема]. Там показывали [что именно: инструмент, фича, процесс].\n\n"
        "Хочу сделать аналогичное. Конкретно:\n"
        "- Что это: [точное описание продукта/сервиса]\n"
        "- Для кого: [целевая аудитория]\n"
        "- Какие функции: [конкретные фичи из поста]\n"
        "- Как работает: [описание процесса из поста]\n\n"
        "Проведи разведку:\n"
        "1. Найди на GitHub готовые решения по теме [ключевые слова из поста] — топ-5 по звёздам\n"
        "2. Найди конкурентов и аналоги — что уже существует на рынке\n"
        "3. Определи оптимальный стек: какие библиотеки/API/сервисы нужны\n"
        "4. Дай пошаговый план реализации с оценкой сроков\n"
        "5. Укажи бюджет: бесплатные vs платные варианты\n"
        "6. Для каждой опции — плюсы, минусы, сложность\n\n"
        "Важно: я не хочу отвечать на вопросы. Выдай полный результат сразу.\n\n"
        "ОГРАНИЧЕНИЯ: до 1200 символов, максимум конкретики.\n\n"
        "--- ИСХОДНЫЙ ПОСТ ---\n"
        f"Аккаунт: {account}\n"
        f"Лайки: {likes} | Комментарии: {comments}\n"
        f"Текст/разбор поста:\n{caption or 'Нет текста'}\n\n"
        f"--- АНАЛИЗ ---\n{analysis}\n\n"
        "Создай промпт. Вытащи ВСЕ конкретные названия, инструменты и детали из поста. "
        "Начни с подходящей роли агента."
    )
